public class logical_operators {
    public static void main(String[] args) {
        // logical and operators :- true only if both expression1 and expression2 are true
            boolean FirstExperssion = false;
            boolean SecondExperssion = true;
            System.out.println(FirstExperssion && SecondExperssion);
        // or operators:- true if either expression1 or expression2 is true
           System.out.println(FirstExperssion||SecondExperssion);
        // Not operators:- true if expression is false and vice versa
          System.out.println(!FirstExperssion);
    }    
}
