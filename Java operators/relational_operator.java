public class relational_operator{
    public static void main(String[] args) {
        int firstNumber =12;
        int secondNumber = 16;
        System.out.println(firstNumber==secondNumber);
        System.out.println(firstNumber!=secondNumber);
        System.out.println(firstNumber>secondNumber);
        System.out.println(firstNumber<secondNumber);
        System.out.println(firstNumber>=secondNumber);
        System.out.println(firstNumber<=secondNumber);
    }
}