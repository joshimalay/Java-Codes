
public class Assignment_operator {
    public static void main(String[] args) {
        int a =12;
        int b =78;
        a +=b; // a = a+b
        System.out.println(a);
        a -=b; // a = a-b
        System.out.println(a);
        a *=b;
        System.out.println(a);
        a /=b;
        System.out.println(a);
        a %=b;
        System.out.println(a);
    }
}
